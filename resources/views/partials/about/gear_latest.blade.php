        <h3>Ukuleles</h3>

        <p>Both the <a class="underline-brand" href="//learningukulele.com/listings/Koolau"> @svg('solid/star', 'text-yellow-600') <b>Ko'olau</b></a>, <a class="underline-brand" href="//learningukulele.com/listings/Pono"> @svg('solid/star', 'text-yellow-600') <b>Pono</b></a> and MyaMoe ukuleles have a radius fingerboard which does make a big difference in ease of playing. The Ko'olau, Pono and MyaMoe are all strung with a low G. For high G, C tuning I grab an American made all Koa Lanakia. Other than Pono I've only found the <a class="underline-brand" href="//learningukulele.com/listings/Godin"><b>Godin Multiuke</b></a> @svg('link') to have a radius fingerboard in a mass produced ukulele.</p>

        <p>And with our own music store, ( <a class="underline-brand" href="//funkyfrets.com"><b>Funky Frets</b>, Boyertown, PA USA</a> @svg('link') ) &ndash; that is naturally, heavy into ukuleles and over 100 ukuleles in stock at all times. I'm in a position to play pretty much what I want — but it's the Ko'olau CS Tenor that gets the most action. That and a Pono solid-body are the only performance ukuleles I perform with in concert.</p>

