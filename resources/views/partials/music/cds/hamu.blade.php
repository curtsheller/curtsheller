<h4 class="m-t">Milestones <small>&bull; Miles Davis</small></h4>
    <p>An up tempo modal song in G Dorian and A Aeolian. We typically play this a break-neck tempo. First take got away from us and this one is bit more under control. Recorded with a Pono Thinline Mango tenor as the previous song I used my Ko'olau CS tenor ( <em>pictured above</em> ) and string one broke right at the end of the song. Luckily it was in the planned fade out and we had enough for that take. And as any ukulele player knows it takes awhile for ukulele strings to stretch out - hences the switch to the Mango Pono.</p>

    <p>I also recorded it as a trio with guitar in 2002 on my Midnight at the Jazz Cafe CD. Take a listen below for comparison, a little quicker this go around.</p>

