<h4 class="m-t">Georgia ( <em>Georgia on My Mind</em> ) <small>&bull; H. Carmichael, S. Gore</small></h4>
    <p>Lovely, bluesy ballad in the key of F.</p>
    <p>"Georgia on My Mind" is a song by Hoagy Carmichael and Stuart Gorrell, closely associated with the cover version by Ray Charles, a native of Georgia, who recorded it for his 1960 album The Genius Hits the Road. It became the official state song of Georgia in 1979.</p>

    <p>The song was written in 1930 by Hoagy Carmichael (music) and Stuart Gorrell (lyrics). Although it is frequently asserted that the lyrics were written not about the state of Georgia, but rather for Carmichael's sister, Georgia Carmichael, Hoagy Carmichael himself contradicted this view with his recounting of the origin of the song in his second autobiography Sometimes I Wonder. Carmichael wrote that the song was composed when bandleader Frankie Trumbauer suggested that he write about the state of Georgia. According to Carmichael, Trumbauer also suggested the opening lyrics should be "Georgia, Georgia ...", with the remaining lyrics coming from Gorell. Carmichael made no mention at all of his sister in his telling of the song's genesis.</p>

