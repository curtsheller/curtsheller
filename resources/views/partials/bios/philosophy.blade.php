<ul class="list-disc leading-loose mx-8">
    <li>All levels of student are accepted and welcome.</li>
    
    <li>All lesson material for the student is focused on students achieving <em>their</em> goals - not my goals. It's your lesson NOT mine.</li>
    
    <li>My goal is NOT to teach you songs but to teach you the principles that are inherent in every song. This will allow you to learn any song and develop your own repertoire and <em>voice</em>.</li>
    
    <li>Focused, short and frequent practice is often more effective than long practice sessions. </li>
    
    <li>It is not my function to motivate a student but to help then find their own motivation.</li>
    
    <li>Any topic that will help you achieve your goals can be covered in the lessons.</li>
    
    <li>All lessons are customized to the individual students goals. There is NO "One size fits all".</li>
    
    <li>There are no lesson assignments that are mandatory for every student.</li>
    
    <li>Rhythm is an often neglected topic for most teachers and one I consider very important. Through Chuck Anderson's Modular Phonetic Rhythm System you'll learn Rhythm as a second language.</li>
    
    <li>We won't learn "licks" and "riffs". You'll learn the underlying principles behind these "licks" and "riffs". Allowing you to learn, create, and develop your own.</li>
    
    <li>Lessons focus on developing your <b>Mind</b>, <b>Hands</b>, and <b>Ears</b>. Allowing your musical creativity to come out. <em>Spoiler Alert</em>: for stringed instruments and the piano the hands take the longest to develop &ndash; It's the motor skills that take the longest to develop.</li>
    
    <li>Perseverance and persistence are more important than "talent" and "education".</li>
    
    <li>If you show up consistently for the lesson you are *guaranteed* progress. The time spent at a lesson is even more important than practice. Don't let a lack of preparation stop you from getting to your lesson. There is always something valuable to do at a lesson above and beyond the assigned lesson material. There is no secret to learning music and a musical instrument - <em>"What ever you put in you will get out."</em></li>
    
    <li>Lessons are NOT school and the practice you do at home is NOT homework. Think of it as more like exploration vs. practice. It's NOT "Play, Practice, and Rules." It's "Work, Exploration, and the Principles of Music." Duke Ellington said there are two rules in music., <b>Rule 1</b>: there are not rules in music. <b>Rule 2</b>: See rule one."</li>
    
    <li>There are no timetables or deadlines on learning anything - Music is no exception.</li>
    
    <li>Lessons will work on developing your own voice - your own unique personality. There is already a "so-and-so" ( <em>insert your favorite musician here</em> ) out there.</li>
    
    <li>People that give up NEVER achieve their goals. As long as you don't give up, you will be always moving forward to obtaining your goals. Goals often adjust or change - but forward motion and progress is certain with persistence.</li>
    
    <li>I teach to help those who have asked for help. I myself have always studied and learned new material. I personally took lessons from the time I was 9 or 10 and continued until just recently and continue to explore new material and revisit and refine what I've already studies. I studied contemporary and jazz guitar as well as anything music related and the music business that I brought up in lessons with Chuck Anderson for well over 30 years of my adult life.</li>
    
    <li>Generally in music if it sounds good it is good.</li>
    
    <li>Music is not a competition or sport. The old proverb says "It's the journey - not the destination."</li>
    
    <li>Although reading standard music notation is not mandatory but IT'S helpful.</li>
    
    <li>You are not going to learn this or that instrument in 7 days or 30 days. There is no "easy" method, etc... There are no shortcuts but there are many ways to learn efficiently.</li>
    
    <li>Don't forget to build on a solid musical and technique foundation in your quest to get "advanced".</li>
    
    <li>Slow and controlled leads to virtuosity. It's all about efficiency of motion. Work *smarter* not *harder*. Speed is a by-product of performing accurately and in-control.</li>
    
    <li>There are no tests, no grades and no performance reviews. You always go at your own pace.</li>
    
    <li>I recognize and actually ask why each student is taking lessons. I do not assume that everyone has the same goals, reasons or motivations. It's actually great that we can all develop you own voices in music.</li>
</ul>
