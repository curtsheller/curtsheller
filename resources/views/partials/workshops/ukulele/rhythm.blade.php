<h2 class="">Rhythm</h2>
<p class="workshop-level">ALL LEVELS</p>

<p>The often neglected and taken for granted subject of Rhythm. Music is Melody, Harmony and Rhythm. We are drawn, initially to music by melody at a very early age, with the instrument of our voice. With ukulele you typically learn chords - the harmony and never really dive into learning rhythm. Unless you're a drummer we take rhythm for granted.</p>

