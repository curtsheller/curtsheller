<h2 class="">Music Theory - The Principles of Music</h2>
<p class="workshop-level">ALL LEVELS</p>

<p class="text-2xl font-thin">Theory? - Not really theory, it's pretty much how music works. So it more of the <em><b>Principles of Music</b></em>.</p>

<p>This workshop starts with the basic principles of music and builds on a that solid foundation.</p>

<p>Topics covered will include:</p>

<ul>
    <li><b>Notes</b>, <b>Pitch</b>, <b>Tempo</b> and <b>Direction in Music</b></li>
    <li><b>Scales</b>, <b>Intervals</b>, <b>Arpeggios</b>, <b>Sequences</b></li>
    <li><b>Chords</b> - Names, building and where do they actually come from.</li>
    <li><b>Rhythm</b> - And how it relates to strums</li>
</ul>
