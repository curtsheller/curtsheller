<h2 class="">Triads - You Already Know More Than You Think</h2>
<p class="workshop-level">ALL LEVELS</p>

<p>At the heart of just about every chord is a <em>triad</em>. This workshop explores these powerful harmonic and melodic music gems.</p>

<p>We'll explore traditional and contemporary triads, starting with the triads you already know but don't know you already know. Finally learning what a sus2, sus4, add 2, add9 chord is. And, how to use them.</p>

<p>Triads can be used for everything from creating melodies and improvisation with single notes and chords all the way to creating contemporary songs.</p>

<p>You actually all ready know a few if you know the open position chords, such as C, F, G, Am, Em, Dm, etc...</p>

