<h3>Common Chord Progressions and Remembering Songs</h3>

<p>This workshop goes over common chord progressions and chord sequences that show up a lot in popular music played at jams and festivals.</p>

<p>We'll cover:</p>

<x-ui.list leading='loose'>
    <li>Three Chord Progressions</li>
    <li>Four Chord Pop Progressions</li>
    <li>Fifties (1950s) Chord Progressions</li>
    <li>The Andalusian Chord Progression</li>
    <li>Blues Chord Progressions</li>
    <li>Backdoor Chord Progressions</li>
</x-ui.list>

<p>Remembering a song is lot like remembering the directions for a road trip. There are the turn-by-turn directions, road maps, signs and landmarks that will get you to where you are going. Turn here, turn there, remember this and remember that landmark. With a songs it's the chords, the melody, style, the harmonic cells, the form, etc that are part of the song that you want to remember.</p>

<p>With a few music tools and an understanding of the basic principles of how chords and chord progressions - the songs work. You can start unraveling what's going on in any song.</p>
