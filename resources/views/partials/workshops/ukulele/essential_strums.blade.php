    <p class="text-2xl font-brand-400 font-light leading-snug">Don't know what to play or tired of playing the same old down-up-down-up strums?</p>

    <p>One of the first skills a ukulele player learns is the art and craft of strumming, playing rhythm. This refers to an accompaniment technique suitable for the singer - songwriter or someone who plays a support role for another instrument.</p>

    <p>Strumming requires a specific set of skills. They are: 1) Memorization of chords 2) The ability to switch chords smoothly and 3) The ability to choose and execute a suitable rhythmic strum. It is this 3rd skill that is our focus in <b>A Guide to Ukulele Strums</b>.</p>

    <p>Though strumming looks natural to the casual observer, it is anything but natural to the beginning ukulele player. Even experienced players have difficulty in identifying and executing certain strums. Though this is one of those topics that is typically taken for granted, there is much to learn about rhythmic feels, accents, dynamics, strum direction, feel, percussive accents, idiomatic styles and tempo variation.</p>


